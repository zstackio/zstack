    void testUpdateLargeMemoryFailure() {
        VmSpec spec = env.specByName("vm")
        UpdateVmInstanceAction updateVmInstanceAction = new UpdateVmInstanceAction()
        updateVmInstanceAction.uuid = spec.inventory.uuid
        updateVmInstanceAction.cpuNum = 10
        updateVmInstanceAction.memorySize = SizeUnit.GIGABYTE.toByte(1025)
        updateVmInstanceAction.sessionId = adminSession()
        UpdateVmInstanceAction.Result res = updateVmInstanceAction.call()
        assert res.error != null

